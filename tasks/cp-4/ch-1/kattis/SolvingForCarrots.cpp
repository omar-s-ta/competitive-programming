#include <bits/stdc++.h>

using namespace std;

class SolvingForCarrots {
public:
  void solve() {
    int n, p;
    in >> n >> p;
    while (n--) {
      string s;
      getline(in, s);
    }
    out << p << '\n';
  }

  SolvingForCarrots(istream& in, ostream& out)
    : in(in), out(out) { }

private:
  ostream& out;
  istream& in;
};